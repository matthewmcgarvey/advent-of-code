package com.mmcgarvey.advent.day1;

public class IntPair {
    private final int left;
    private final int right;

    public IntPair(int left, int right) {
        this.left = left;
        this.right = right;
    }

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }
}
